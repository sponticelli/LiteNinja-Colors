        // Create a color from a color name.
        public static Color FromName(string name)
        {
            return NamedColors.ContainsKey(name.ToLower()) ? NamedColors[name] : Color.black;
        }

        // Find color name that match a given color.
        public static string FindName(Color color)
        {
            return NamedColors
                .Where(c => c.Value == color)
                .Select(c => c.Key)
                .FirstOrDefault();
        }

        // Find the closest color name in the CssColors dictionary.
        public static string FindClosestName(Color color)
        {
            var minDist = float.MaxValue;
            var closest = "black";
            foreach (var c in NamedColors)
            {
                var tmp = c.Value - color;
                var dist = tmp.r * tmp.r + tmp.g * tmp.g + tmp.b * tmp.b;
                if (dist > minDist) continue;
                minDist = dist;
                closest = c.Key;
            }

            return closest;
        }

        // Find the closest color in the Colors dictionary.
        public static Color FindClosestNamedColor(Color color)
        {
            var minDist = float.MaxValue;
            var closest = Color.black;
            foreach (var c in NamedColors)
            {
                var tmp = c.Value - color;
                var dist = tmp.r * tmp.r + tmp.g * tmp.g + tmp.b * tmp.b;
                if (dist > minDist) continue;
                minDist = dist;
                closest = c.Value;
            }

            return closest;
        }
        
        // Create color name from HSL values.
        public static string NameFromHSL(ColorHSL color)
        {
            var name = "";

            switch (color.Lightness)
            {
                // Lightness.
                case 0:
                    return "black";
                case 1:
                    return "white";
            }

            name += color.Lightness switch
            {
                < 0.1f => "blackish ",
                < 0.25f => "dark ",
                < 0.5f => "darkish ",
                < 0.65f => "",
                < 0.70f => "lightish ",
                < 0.75f => "light ",
                < 0.9f => "very light ",
                _ => "whitish ",
            };
            

            name += color.Saturation switch
            {
                // Saturation
                < 0.1f => "grey ",
                < 0.25f => "grayish ",
                < 0.5f => "medium grey ",
                < 0.65f => "medium ",
                < 0.70f => "medium bright ",
                < 0.75f => "bright ",
                < 0.9f => "very bright ",
                _ => "saturated "
            };

            name += color.Hue switch
            {
                // Hue
                < 18f/255f => "red",
                < 36f/255f => "orange",
                < 54f/255f => "yellow",
                < 72f/255f => "green",
                < 90f/255f => "cyan",
                < 108f/255f => "blue",
                < 126f/255f => "purple",
                < 144f/255f => "magenta",
                < 162f/255f => "pink",
                < 180f/255f => "rose",
                _ => "red",
            };

            return name;
        }
